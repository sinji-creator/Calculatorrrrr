    private void plusMinusButtonActionPerformed(java.awt.event.ActionEvent evt) {                                                
        // TODO add your handling code here:
        String currentText = numberText.getText();
        if (currentText.equals("0")) {
            numberText.setText("0");
        } else {
           if (currentText.startsWith("-")) {
               numberText.setText(currentText.substring(1));
           } else {
               numberText.setText("-" + currentText); 
           }
        }
        
    }                                               

    private void percentButtonActionPerformed(java.awt.event.ActionEvent evt) {                                              
        // TODO add your handling code here:
        try {
            double currentValue = Double.parseDouble(numberText.getText());
            double percentValue = 0;

            switch (operator) {
                case "+":
                case "-":
                    // Hitung persen dari operand1
                    percentValue = operand1 * (currentValue / 100.0);
                    break;
                case "*":
                case "ร":
                    percentValue = currentValue / 100.0;
                    break;
                case "/":
                case "รท":
                    percentValue = currentValue / 100.0;
                    break;
                default:
                    // Jika belum ada operator, anggap saja currentValue = currentValue / 100
                    percentValue = currentValue / 100.0;
            }

            // Tampilkan hasil persen di field input
            if (percentValue == (int) percentValue) {
                numberText.setText(String.valueOf((int) percentValue));
            } else {
                numberText.setText(String.valueOf(percentValue));
            }

        } catch (NumberFormatException e) {
            numberText.setText("Error");
        }
        
    }                                             

