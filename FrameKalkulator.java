    private void enterButtonActionPerformed(java.awt.event.ActionEvent evt) {                                            
        // TODO add your handling code here:
        operand2 = Double.parseDouble(numberText.getText());

        if (operator.equals("+")) {
            // Cek apakah operand1 dan operand2 adalah bilangan bulat
            boolean isOperand1Int = (operand1 == (int) operand1);
            boolean isOperand2Int = (operand2 == (int) operand2);

            if (isOperand1Int && isOperand2Int) {
                // Jika keduanya integer, tampilkan hasil sebagai int
                int result = (int) (operand1 + operand2);
                resultText = String.valueOf(result);
            } else {
                // Jika salah satu ada desimal, tampilkan hasil sebagai double
                double result = operand1 + operand2;
                // Cek apakah hasilnya adalah bilangan bulat dalam bentuk double
                if (result == (int) result) {
                    // Tampilkan tanpa desimal
                    resultText = String.valueOf((int) result);
                } else {
                    // Tampilkan hasil dengan desimal
                    resultText = String.valueOf(result);
                }
            }
        } else if (operator.equals("-")) {
            // Cek apakah operand1 dan operand2 adalah bilangan bulat
            boolean isOperand1Int = (operand1 == (int) operand1);
            boolean isOperand2Int = (operand2 == (int) operand2);

            if (isOperand1Int && isOperand2Int) {
                // Jika keduanya integer, tampilkan hasil sebagai int
                int result = (int) (operand1 - operand2);
                resultText = String.valueOf(result);
            } else {
                // Jika salah satu ada desimal, tampilkan hasil sebagai double
                double result = operand1 - operand2;
                // Cek apakah hasilnya adalah bilangan bulat dalam bentuk double
                if (result == (int) result) {
                    // Tampilkan tanpa desimal
                    resultText = String.valueOf((int) result);
                } else {
                    // Tampilkan hasil dengan desimal
                    resultText = String.valueOf(result);
                }
            }
        } else if (operator.equals("*")) {
            // Cek apakah operand1 dan operand2 adalah bilangan bulat
            boolean isOperand1Int = (operand1 == (int) operand1);
            boolean isOperand2Int = (operand2 == (int) operand2);

            if (isOperand1Int && isOperand2Int) {
                // Jika keduanya integer, tampilkan hasil sebagai int
                int result = (int) (operand1 * operand2);
                resultText = String.valueOf(result);
            } else {
                // Jika salah satu ada desimal, tampilkan hasil sebagai double
                double result = operand1 * operand2;
                // Cek apakah hasilnya adalah bilangan bulat dalam bentuk double
                if (result == (int) result) {
                    // Tampilkan tanpa desimal
                    resultText = String.valueOf((int) result);
                } else {
                    // Tampilkan hasil dengan desimal
                    resultText = String.valueOf(result);
                }
            }
        } else if (operator.equals("/")) {
            // Cek pembagian dengan nol
            if (operand2 == 0) {
                resultText = "Undefined";  // atau "Error", atau tampilkan alert
            } else {
                boolean isOperand1Int = (operand1 == (int) operand1);
                boolean isOperand2Int = (operand2 == (int) operand2);

                if (isOperand1Int && isOperand2Int) {
                    // Jika keduanya integer, hasil dibulatkan (int)
                    int result = (int) (operand1 / operand2);
                    resultText = String.valueOf(result);
               } else {
                    // Hasil dalam bentuk double
                    double result = operand1 / operand2;

                    // Cek jika hasilnya bilangan bulat
                    if (result == (int) result) {
                        resultText = String.valueOf((int) result);
                    } else {
                        resultText = String.valueOf(result); 
                    }
                }
            }
        } else {
            // Logika untuk operator lain jika ada
            resultText = "0"; // default atau bisa disesuaikan
        }

        // Tampilkan hasil ke layar
        numberText.setText(resultText);
        
        try {
            operand1 = Double.parseDouble(resultText);
        } catch (NumberFormatException e) {
            operand1 = 0; // reset jika hasilnya bukan angka (misal "Undefined")
        } 
    }                                           

